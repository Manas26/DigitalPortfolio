// for first video
var vid1 = document.getElementById('video1');
vid1.addEventListener('touchstart', function () {
    vid1.paused = "true";


    if (vid1.paused) {
        vid1.play();

    }
    else {
        vid1.pause();

    }
});
// Ending of first video

